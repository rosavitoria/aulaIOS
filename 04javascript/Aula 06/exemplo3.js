//o sistema calcula a media, dependendo é reprovado ou aprovado
//media de 7.0

let notas = [4 + 7 + 8 + 4]
let media = notas / 4

if (media >= 7) {
    console.log('Média de', media, 'aprovado');
}
else if (media >5) {
    console.log('Média de', media, 'recuperação');
}
else {
    (media <=5)
    console.log('Média de', media, 'reprovado');
}

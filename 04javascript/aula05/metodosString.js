let nome = 'Rosa Vitória'
console.log(nome[0]);
console.log(nome.length);           //retorna a quantidade de caracteres presentes na string

console.log(nome.charAt(1));        //charArt acessa determinado caractere baseado no indice escolhido

console.log(nome.toLowerCase());    //transforma todos os caracteres em minusculo

console.log(nome.toUpperCase());    //transforma todos os caracteres em maiusculo

console.log(nome.substring(0,4));   //corta a string do primeiro ao segundo indice

console.log(nome.split(' '));       //separa a string de acordo com o que você especificou